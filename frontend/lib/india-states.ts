/** Returns an error string if pincode is invalid for India, else null */
export function validateIndiaPincode(pin: string): string | null {
  if (!pin) return null;
  if (!/^\d{6}$/.test(pin)) return 'Pincode must be exactly 6 digits';
  return null;
}
